import UIKit

/* Returns sum of two double variables */
func add(num1: Double, num2: Double) -> Double { // 2
    return num1 + num2
}

/* Returns difference of two double variables */
func subtract(num1: Int, num2: Int) -> Int { // 3
    return num1 - num2
}

/* Returns multiplicaiton value of two double variables */
func multiplication(num1: Float, num2: Float) -> Float { // 4
    return num1 * num2
}

/* Returns division value of two double variables */
func division(num1: Double, num2: Double) -> Double { // 5
    return num1 / num2
}

// 6
print(            add(num1: 4, num2: 2) )
print(       subtract(num1: 4, num2: 2) )
print( multiplication(num1: 4, num2: 2) )
print(       division(num1: 4, num2: 2) )
